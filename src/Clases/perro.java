public class perro {
    public String comidadP;
    public double pesoP;
    public String colorP;
    public int aniosP;
    public perro() {
        comidadP="5 veces al dia";
        pesoP=26.5;
        colorP="negro";
        aniosP=5;
    }
    public perro(String comidadP, double pesoP, String colorP, int aniosP) {
        this.comidadP = comidadP;
        this.pesoP = pesoP;
        this.colorP = colorP;
        this.aniosP = aniosP;
    }


}
